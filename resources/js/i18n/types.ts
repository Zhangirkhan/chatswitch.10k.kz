type LeafPaths<T, Prefix extends string = ''> = T extends string
    ? Prefix
    : {
          [K in keyof T & string]: LeafPaths<T[K], Prefix extends '' ? K : `${Prefix}.${K}`>;
      }[keyof T & string];

export type MessageKey = LeafPaths<MessageCatalog>;

